#include<iostream>
using namespace std;
int main(){

    // for loop syntax..
    for(int i = 0; i <= 10; i++){
        cout<<i<<" ";
    }

    // while loop syntax..
    int i = 0;  // initialisation..
    while(i <= 10){     // condition checking..
        cout<<i<<" ";
        i++;     // increment/decrement..
    }
}